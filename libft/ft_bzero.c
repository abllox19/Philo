#include "libft.h"

void	*ft_bzero(void *b, size_t len)
{
	size_t			i;
	unsigned char	*ptr;

	ptr = b;
	i = 0;
	while (i < len)
	{
		ptr[i] = 0;
		i++;
	}
	return (b);
}
